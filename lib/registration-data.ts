/**
 * Data for international registration: countries with dial codes and ISO codes for flags,
 * institutions (universities), and programs of study.
 */

export interface CountryWithDial {
  name: string;
  dialCode: string;
  /** ISO 3166-1 alpha-2 (e.g. US, IN) for flag emoji */
  iso2: string;
  /** ISO 3166-1 alpha-3 (e.g. USA, IND, GBR) for short display */
  iso3: string;
}

/** Returns Unicode flag emoji for ISO 2-letter country code (e.g. "US" → 🇺🇸) */
export function getCountryFlag(iso2: string): string {
  if (!iso2 || iso2.length !== 2) return "";
  const u = iso2.toUpperCase();
  return String.fromCodePoint(
    ...u.split("").map((c) => 0x1f1e6 - 65 + c.charCodeAt(0))
  );
}

