export interface CountryWithDial {
  name: string;
  dialCode: string;
  iso2: string;
  iso3: string;
}

function getIso3(iso2: string): string {
  return ISO3[iso2.toUpperCase()] || iso2.toUpperCase();
}

/** Get short display code for phone selector: IND, USA, GBR, AFG, PAK */
export function getCountryCode(iso2: string): string {
  return getIso3(iso2);
}

