/** Training payload from GET /api/chatbot/training-data */
export type TrainingPayload = {
  about: Record<string, unknown>;
  accommodation: { summary: string };
  contact: { summary: string; categories: Array<{ name: string; coordinators: Array<{ name: string; phone: string; email: string }> }> };
  sponsors: Array<{ name: string; description: string; website: string | null }>;
  competitions: Array<{
    name: string;
    category: string;
    categorySlug: string;
    slug: string;
    description: string;
    termsAndConditions: string;
    virtualTermsAndConditions: string | null;
    venue: string;
    date: string;
    startTime: string;
    endTime: string;
    isGroupEvent: boolean;
    minTeamSize: number;
    maxTeamSize: number;
    participantLimit: number;
    virtualEnabled: boolean;
  }>;
  staticKnowledgeBlock: string;
};

/** Build full knowledge string for the AI from training payload (includes all competitions with descriptions and terms). */
export function buildFullKnowledgeForAI(payload: TrainingPayload): string {
  const parts: string[] = [payload.staticKnowledgeBlock];

  if (payload.sponsors.length > 0) {
    parts.push(
      "\n## Sponsors\n" +
        payload.sponsors
          .map((s) => `- ${s.name}${s.description ? ": " + s.description : ""}`)
          .join("\n")
    );
  }

  if (payload.contact.categories.length > 0) {
    parts.push(
      "\n## Contact Coordinators\n" +
        payload.contact.categories
          .map(
            (cat) =>
              `${cat.name}: ${cat.coordinators.map((c) => `${c.name} (${c.email})`).join("; ")}`
          )
          .join("\n")
    );
  }

  if (payload.competitions.length > 0) {
    parts.push("\n## Competitions (with descriptions and terms)\n");
    for (const c of payload.competitions) {
      parts.push(`### ${c.name} (Category: ${c.category})`);
      parts.push(`Description: ${c.description || "—"}`);
      parts.push(`Venue: ${c.venue} | Date: ${c.date} | Time: ${c.startTime} - ${c.endTime}`);
      if (c.isGroupEvent) {
        parts.push(`Team size: ${c.minTeamSize}-${c.maxTeamSize} members.`);
      }
      if (c.termsAndConditions && c.termsAndConditions.trim()) {
        parts.push(`Terms and conditions:\n${c.termsAndConditions.trim()}`);
      }
      if (c.virtualEnabled && c.virtualTermsAndConditions && c.virtualTermsAndConditions.trim()) {
        parts.push(`Virtual participation terms:\n${c.virtualTermsAndConditions.trim()}`);
      }
      parts.push(""); // blank line between events
    }
  }

  return parts.join("\n").trim();
}
